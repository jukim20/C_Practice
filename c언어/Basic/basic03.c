#include <stdio.h>
void main(){
	printf("!!");
	// 산술연산 ( + - * / ) ==> 더하기 빼기 곱하기 나누기
	printf(" 2 + 2 \n");  // 2 + 2
	printf(" 2 - 2 \n");  // 2 - 2
	printf(" 2 * 2 \n");  // 2 * 2
	// 서식문자 %d ==> , 뒤에있는 숫자가 표현된다.
	printf(" %d \n" , 2 + 2);  // 4 
	// 문제 1) 산술연산  + - * / 를 서식문자를 사용해서 표현해보세요 .
	// 문제 2) 10 + 2 = 12  ==> %d 를 3개 사용해보세요.
	printf("%d + %d = %d\n" , 10, 2, 12);
	// 수업 진도 ...


}